def limma(limma_data,r_lib_loc=None,force=False):
	if r_lib_loc in ["None","./"]:
		r_lib_loc = "/usr/lib/R/lib/"
	
	try:
		design_list = [] 
		data_column_list = [] 

		for data_type in ['wildtype','mutant']:
			for replicate_number in range(0,len(limma_data[list(limma_data.keys())[0]]['wildtype'])):
				replicate_number = str(replicate_number + 1)
				design_list.append([data_type+'_'+replicate_number,data_type])
				data_column_list.append(data_type+'_'+replicate_number)

		design = pd.DataFrame(design_list,columns = ['Sample Identifier','TimePoint']) 
		
		###------------------------------------###

		data_list = []

		for peptide_id in limma_data:
			data_list_row = [peptide_id]
			data_list_row += limma_data[peptide_id]['wildtype']
			data_list_row += limma_data[peptide_id]['mutant']
			data_list.append(data_list_row)

		data = pd.DataFrame(data_list,columns = ['Name'] + data_column_list) 
		data = data.set_index('Name') 

		###------------------------------------###

		#Import R libraries
		utils = importr('utils')
		base = importr('base')
		stats = importr('stats')
		
		if force:
			utils.chooseCRANmirror(ind=1)
			try:
				#robjects.r('install.packages("BiocManager", ''repos="https://cran.r-project.org")')
				robjects.r('install.packages("BiocManager", ''repos="https://cran.ma.imperial.ac.uk/")')
			except:
				logger.error("BiocManager installation failed")
				pass
			
			biocManager = importr("BiocManager")
		else:
			try:
				logger.debug("Importing BiocManager")
				biocManager = importr("BiocManager")
			except:
				utils.chooseCRANmirror(ind=1)

				logger.debug("Installing BiocManager")
				#robjects.r('install.packages("BiocManager", ''repos="http://cran.r-project.org")')
				robjects.r('install.packages("BiocManager", ''repos="https://cran.ma.imperial.ac.uk/")')

				logger.debug("Importing BiocManager")
				biocManager = importr("BiocManager")

		try:
			limma = importr('limma', lib_loc=r_lib_loc)
		except:
			biocManager.install('limma', lib_loc=r_lib_loc, lib=r_lib_loc,force = force)
			limma = importr('limma', lib_loc=r_lib_loc)

		###------------------------------------###

		# Convert data and design pandas dataframes to R dataframes
		with localconverter(ro.default_converter + pandas2ri.converter):
			r_data = ro.conversion.py2rpy(data)
			r_design = ro.conversion.py2rpy(design)
			names = ro.StrVector([ str(index) for index in data.index.tolist()])

		# Create a model matrix using design's TimePoint column using the R formula "~0 + f" to get all the unique factors as columns
		f = base.factor(r_design.rx2('TimePoint'), levels=base.unique(r_design.rx2('TimePoint')))
		form = Formula('~0 + f')
		form.environment['f'] = f
		r_design = stats.model_matrix(form)
		r_design.colnames = base.levels(f)

		fit = limma.lmFit(r_data, r_design)

		# Make a contrasts matrix with the 1st and the last unique values
		contrast_matrix = limma.makeContrasts(f"{r_design.colnames[0]}-{r_design.colnames[-1]}", levels=r_design)

		# Fit the contrasts matrix to the lmFit data & calculate the bayesian fit
		fit2 = limma.contrasts_fit(fit, contrast_matrix)
		fit2 = limma.eBayes(fit2)

		# topTreat the bayesian fit using the contrasts and add the genelist
		r_output = limma.topTreat(fit2, coef=1, genelist=names, number=np.inf)

		#print(r_output)
		p_value = list(r_output.rx2('P.Value'))
		p_value_adj = list(r_output.rx2('adj.P.Val'))
		#print(p_value_adj)
		log_p_value = [-math.log(x,10) for x in list(r_output.rx2('P.Value'))] 
		log_p_value_adj = [-math.log(x,10) for x in list(r_output.rx2('adj.P.Val'))]
		logFC = list(r_output.rx2('logFC'))
		names = list(r_output.rx2('ID'))

		limma_scores = {}
		for i in range(0,len(names)):
			limma_scores[names[i]] = {
				"log_fold_change":logFC[i],
				"log_p_value":log_p_value[i],
				"p_value_adj":p_value_adj[i],
				"log_p_value_adj":log_p_value_adj[i],
				"p_value":p_value[i],
				"wildtype":limma_data[names[i]]['wildtype'],
				"mutant":limma_data[names[i]]['mutant'],
				"log_fold_change_guide":[limma_data[names[i]]['wildtype'][x]-limma_data[names[i]]['mutant'][x] for x in range(0,len(limma_data[names[i]]['wildtype']))]
			}

			limma_scores[names[i]]['log_fold_change_guide_std_dev'] = statistics.stdev(limma_scores[names[i]]['log_fold_change_guide'])
			
		return limma_scores
	except:
		utilities_error.printError()
		return {}


